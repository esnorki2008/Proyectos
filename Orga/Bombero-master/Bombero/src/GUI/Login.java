    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        OperacionesLogin L = new OperacionesLogin();
        if (L.VerificarContrase침a(this.jTextField1.getText(), this.jTextField2.getText())) {
            if (this.jTextField1.getText().toLowerCase().trim().equals("admin")) {
                JOptionPane.showMessageDialog(null, "Ingresando como Administrador");
                GuiAdmin G = new GuiAdmin();
                G.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Ingresando como Usuario: " + this.jTextField1.getText());
                Usuario Usu = new Usuario(this.jTextField1.getText());
                GuiUsuario U = new GuiUsuario(Usu);
                U.setVisible(true);
                this.dispose();

            }
        } else {
            JOptionPane.showMessageDialog(null, "Las Credenciales Son Erroneas");

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        OperacionesLogin Log = new OperacionesLogin();
        if (this.jTextField4.getText().toLowerCase().trim().equals(this.jTextField5.getText().toLowerCase().trim())) {

            JOptionPane.showMessageDialog(null, "Solicitud Cargada Al Sistema");
            Log.VerificarRegistro(this.jTextField3.getText(), this.jTextField4.getText());
        }else{
            JOptionPane.showMessageDialog(null, "Las Contrase침as No Coinciden");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

