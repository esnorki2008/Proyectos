    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
       Edificio Edi=(Edificio)this.Car.getEdificioLST().Find(this.TxtEdificioCodigo.getText());
       if(Edi==null)
           this.Mensaje("No Existe", "No Se Encontro El Edificio "+this.TxtEdificioCodigo.getText());
       else{
            Node Nodito=Edi.getSalonLST().getHead();
            while(Nodito!=null){
                Salon Sal = (Salon) Nodito.getContenido();
                this.Car.BorrarSalonDelB(Sal);
                Nodito=Nodito.getNext();
            }
            this.Car.getEdificioLST().Delete(Edi.getNombre());
            this.Actualizacion();
        }
    }//GEN-LAST:event_jButton6ActionPerformed

