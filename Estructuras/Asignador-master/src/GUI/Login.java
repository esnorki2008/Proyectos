        Usuario Usr=Car.Login(jTextField1.getText(), jTextField2.getText());
        if(this.jTextField1.getText().equalsIgnoreCase("SU") && this.jTextField2.getText().equalsIgnoreCase("SU") ){
            GUI G= new GUI(Car);
            G.setVisible(true);
            this.setVisible(false);
            this.dispose();
        }else if(Usr!=null){
            if(Usr.getTipo().toLowerCase().trim().equals("estudiante")){
                GuiEstudiante GE = new GuiEstudiante(this.Car,Usr);
                GE.setVisible(true);
                this.setVisible(false);
                this.dispose();
            }else if(Usr.getTipo().toLowerCase().trim().equals("colaborador")){
                GUIColaborador GE = new GUIColaborador(this.Car);
                GE.setVisible(true);
                this.setVisible(false);
                this.dispose();
            }else{
                System.out.println(Usr.getTipo()+"  |   " +"Error");
            }
        }else{
            System.out.println("NULO    "+this.jTextField2.getText());
        }        // TODO add your handling code here:
